# Operadores

# Aritméticos

a = 22

b = 1

# Suma
print(a + b)

# Resta
print(a - b)

# Multiplicación
print(a * b)

# División
print(a / b)

# Módulo
print(a % b)

# División entera
print(a // b)

# Elevado a la potencia
print(a**b)

# Lógicos

# And
print(a > 20 and b < 2)

# Or
print(a > 20 or b < 2)

# Not
print(not (a > 20 and b < 2))

# Comparación

# Mayor que
print(a > b)

# Menor que
print(a < b)

# Mayor o igual que
print(a >= b)

# Menor o igual que
print(a <= b)

# Igual que
print(a == b)

# Diferente de
print(a != b)

# De asignación

# incremento
a += 1

# decremento
a -= 1

# multiplicación

a *= 2

# división

a /= 2

# división entera

a //= 2

# módulo

a %= 2

# Estructuras de Control
c = 22
d = 23
# Condicionales

if c > d:
    print("c es mayor que d")
else:
    print("c es menor que d")
